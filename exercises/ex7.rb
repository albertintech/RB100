# What's the major difference between an Array and a Hash?

# An array is a container that uses an index to access elements within.
#
# A hash is also a container, but it uses a key/value pair structure; a key, not an index, is used to access the value paired to that key.
#
# LS Answer: The major difference between an array and a hash is that a hash contains a key value pair for referencing by key.
