What does each method return in the following example?

arr = [15, 7, 18, 5, 12, 8, 5, 1]

1. arr.index(5) #The index method returns the first index that contains the value of the argument, in this case 3. Note that 5 is also at index 6

2. arr.index[5] #This should return an error.

3. arr[5] #Just returns value at index 5, which is 8